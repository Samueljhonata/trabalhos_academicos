    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        // TODO add your handling code here:
        if (!txtQtd.getText().equals("")) {
            String cod = jList1Produtos.getSelectedValue().split(" ")[0];
            ControlaVendas cVendas = new ControlaVendas();
            int qtd = Integer.parseInt(txtQtd.getText());
            if(cVendas.addProdutoLCompras(Integer.parseInt(cod), qtd)){
                setVisible(false);
            }else{
                lblAviso.setText("Não há estoque suficiente");
            }
            
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

