    void atualizaLista() {
        DefaultListModel lista = new DefaultListModel();
        String a[] = new String[1];
        if (!txtDescricao.getText().equals("")) {
            a = cProdutos.retornaProdutos(txtDescricao.getText());
        } else {
            a = cProdutos.retornaProdutos();
        }

        for (int i = 0; i < a.length; i++) {
            lista.addElement(a[i]);
        }
        if (lista.size() == 0) {
            jList1Produtos.setVisible(false);
        } else {
            jList1Produtos.setModel(lista);
            jList1Produtos.setVisible(true);
        }
        btnSelecionar.setEnabled(false);
    }

