    private void btnInicializaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicializaActionPerformed
        // TODO add your handling code here:
        if (txtZona.getText().equals("")) {
            lblAviso.setText("Erro ao inicializar!");
            return;
        }
        ControlaUrna cUrna = new ControlaUrna();
        if (cUrna.inicializaUrna(Integer.parseInt(txtZona.getText()))) {
            lblAviso.setText("Inicializada com sucesso!");
            txtZona.setText("");
        } else {
            lblAviso.setText("Erro ao inicializar!");
        }
        txtZona.requestFocus();
        atualiza();
    }//GEN-LAST:event_btnInicializaActionPerformed

