    public void atualiza() { //lista as urnas j√° cadastradas
        DefaultListModel lista = new DefaultListModel();
        String a[] = cUrna.listaUrnas();

        for (int i = 0; i < a.length; i++) {
            lista.addElement("Zona Eleitoral n. ".concat(a[i]));
        }
        if (lista.size() == 0) {
            listaUrnas.setVisible(false);
        } else {
            listaUrnas.setModel(lista);
            listaUrnas.setVisible(true);
        }
    }

