    public void atualizaComboBox() {//zonas disponíveis
        DefaultListModel lista = new DefaultListModel();
        String a[] = cUrna.listaUrnas();
        cbZona.removeAll();
        cbZona.setSelectedItem(null);
        for (int i = 0; i < a.length; i++) {
            lista.addElement(a[i]);
            cbZona.addItem(a[i]);
        }
        if (lista.size() == 0) {
            cbZona.setVisible(false);
        } else {
            cbZona.setVisible(true);
        }

    }

