    private void txtTituloKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTituloKeyReleased
        // TODO add your handling code here:
        Eleitor eleitor = cUrna.buscaEleitor(txtTitulo.getText());
        if (eleitor != null) {
            lblNome.setText(eleitor.getNome());
            lblZona.setText(Integer.toString(eleitor.getZonaEleitoral()));
        } else {
            lblNome.setText("Não encontrado");
            lblZona.setText("-");
        }
    }//GEN-LAST:event_txtTituloKeyReleased

