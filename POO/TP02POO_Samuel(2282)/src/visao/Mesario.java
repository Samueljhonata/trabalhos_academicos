    private void btnLiberaVotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLiberaVotoActionPerformed
        // TODO add your handling code here:
        cUrna.listaEleitores();
        int zona = Integer.parseInt(cbZona.getSelectedItem().toString());
        if (cUrna.liberaVotoPrefeito(txtTitulo.getText(), zona)) {
            Eleitor eleitor = cUrna.buscaEleitor(txtTitulo.getText());
            UrnaVotacao urnaVotacao = new UrnaVotacao(zona, eleitor);
            urnaVotacao.setVisible(true);
            this.dispose();
            this.setDefaultCloseOperation(this.EXIT_ON_CLOSE);
        } else {
            lblAviso.setText("Não é possível votar");
        }

    }//GEN-LAST:event_btnLiberaVotoActionPerformed

