    public void limpaCampos() {
        txtCPF.setText("");
        txtEmail.setText("");
        txtNome.setText("");
        txtTel.setText("");
        lblFaltaCPF.setText("");
        lblFaltaEmail.setText("");
        lblFaltaNome.setText("");
        lblFaltaTel.setText("");
    }

    public void bloqueiaCampos() {
        txtCPF.setEditable(false);
        txtEmail.setEditable(false);
        txtNome.setEditable(false);
        txtTel.setEditable(false);
        txtCPF.enable(false);
        txtEmail.enable(false);
        txtNome.enable(false);
        txtTel.enable(false);
    }

    public void desbloqueiaCampos() {
        txtCPF.setEditable(true);
        txtEmail.setEditable(true);
        txtNome.setEditable(true);
        txtTel.setEditable(true);
        txtCPF.enable(true);
        txtEmail.enable(true);
        txtNome.enable(true);
        txtTel.enable(true);
    }

    public void atualizaLista() {
        DefaultListModel lista = new DefaultListModel();
        String a[] = controle.pegaCPFeNome();
        for (int i = 0; i < a.length; i++) {
            lista.addElement(a[i]);
        }
        if (lista.size() == 0) {
            listaUsuarios.setVisible(false);
            lblNenhumUsuario.setVisible(true);
        } else {
            listaUsuarios.setModel(lista);
            lblNenhumUsuario.setVisible(false);
        }

    }

