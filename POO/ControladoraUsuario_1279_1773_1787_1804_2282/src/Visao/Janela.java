    private void listaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaUsuariosMouseClicked
        // TODO add your handling code here:
        String valor = listaUsuarios.getSelectedValue();
        Usuario usuario = null;
        String cpf = "";
        cpf = valor.split(" ")[0]; //pega o cpf dentro da string
        usuario = controle.pesquisaUsuario(cpf);
        limpaCampos();
        bloqueiaCampos();
        btnCancelar.setVisible(false);
        btnInserir.setVisible(false);
        lblTitulo.setText("Pesquisando");
        lblAviso.setText("");
        if (usuario != null) {
            txtNome.setText(usuario.getNome());
            txtCPF.setText(usuario.getCpf());
            txtEmail.setText(usuario.getEmail());
            txtTel.setText(usuario.getTelefone());
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
        } else {
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnSalvar.setEnabled(false);
        }
        bloqueiaCampos();
    }//GEN-LAST:event_listaUsuariosMouseClicked

