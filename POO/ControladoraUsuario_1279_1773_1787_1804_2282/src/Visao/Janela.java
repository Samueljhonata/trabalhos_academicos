    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        txtNome.requestFocus();
        desbloqueiaCampos();
        limpaCampos();
        lblTitulo.setText("Inserindo Usuario");
        listaUsuarios.setVisible(false);
        btnInserir.setVisible(true);
        btnCancelar.setVisible(true);        // TODO add your handling code here:
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        String cpf = txtCPF.getText();
        if (controle.removeUsuario(cpf)) {
            lblAviso.setText("Removido com sucesso!");
            lblAviso.setForeground(blue);
        } else {
            lblAviso.setText("Não foi possível remover");
            lblAviso.setForeground(red);
        }
        limpaCampos();
        bloqueiaCampos();
        atualizaLista();
        btnInserir.setVisible(false);
        btnSalvar.setVisible(false);
        btnCancelar.setVisible(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

